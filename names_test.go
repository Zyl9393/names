func TestRemove(t *testing.T) {
	n := New(2, true)
	for _, name := range randomWords2500 {
		n.Add(name)
	}

	expected := n.Find("en", nil)

	removeIndex := -1
	for i, name := range expected {
		if name == "adVErtisEMents" {
			removeIndex = i
		}
	}
	expected = append(expected[:removeIndex], expected[removeIndex+1:]...)
	removeIndex = -1
	for i, name := range expected {
		if name == "goUVerNEment" {
			removeIndex = i
		}
	}
	expected = append(expected[:removeIndex], expected[removeIndex+1:]...)

	slices.Sort(expected)
	if !n.Remove("adVErtisEMents") {
		t.Fatal(`Remove("adVErtisEMents") failed unexpectedly.`)
	}
	if !n.Remove("goUVerNEment") {
		t.Fatal(`Remove("adVErtisEMents") failed unexpectedly.`)
	}
	result := n.Find("en", nil)
	slices.Sort(result)
	if !reflect.DeepEqual(result, expected) {
		t.Errorf("Resulting slice (length %d) did not match expected slice (length %d).", len(result), len(expected))
	}
}

func TestFindWithBuffer(t *testing.T) {
	for maxLookupSubstringRuneCount := 1; maxLookupSubstringRuneCount <= 3; maxLookupSubstringRuneCount++ {
		n := New(maxLookupSubstringRuneCount, false)
		for _, name := range randomWords2500 {
			n.Add(name)
		}
		bigEnoughBuf := make([]string, 0, 2500)
		returnedBufA := n.Find("en", bigEnoughBuf)
		if &returnedBufA[:1][0] != &bigEnoughBuf[:1][0] {
			t.Errorf(`n.Find("en", bigEnoughBuf) != bigEnoughBuf for maxLookupSubstringRuneCount = %d`, maxLookupSubstringRuneCount)
		}
		if len(returnedBufA) == 0 {
			t.Errorf(`len(returnedBuf) = 0 for maxLookupSubstringRuneCount = %d`, maxLookupSubstringRuneCount)
		}
		const numResultsExpected = 259
		if len(returnedBufA) != numResultsExpected {
			t.Errorf(`len(returnedBuf) == %d for maxLookupSubstringRuneCount = %d; expected %d.`, len(returnedBufA), maxLookupSubstringRuneCount, numResultsExpected)
		}

		tooSmallBuf := make([]string, 0, 5)
		returnedBufB := n.Find("en", tooSmallBuf)
		if &returnedBufB[:1][0] == &tooSmallBuf[:1][0] {
			t.Errorf(`n.Find("en", tooSmallBuf) == tooSmallBuf for maxLookupSubstringRuneCount = %d`, maxLookupSubstringRuneCount)
		}

		if !reflect.DeepEqual(returnedBufA, returnedBufB) {
			t.Errorf(`n.Find("en", bigEnoughBuf) != n.Find("en", tooSmallBuf) for maxLookupSubstringRuneCount = %d`, maxLookupSubstringRuneCount)
		}
	}
}

